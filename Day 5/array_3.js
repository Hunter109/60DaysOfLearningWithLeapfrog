// shift unshift method in array 

// unshift() method is use to add element at the start of the array 
let bike = ["honda", "beneli", "ktm", "pulsar"];
bike.unshift("mt-15");
bike.unshift("crossfire");
console.log(bike);
console.log(`the length of the array after unshift is ${bike.length}`)


//shift() method is use to remove element at the start of the array 
bike.shift();
console.log(bike)
console.log(`the length of the array after shift  is ${bike.length}`)




