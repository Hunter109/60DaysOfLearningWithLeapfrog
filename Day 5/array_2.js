// array method in javascript 

let carCompany = ["volvo", "toyota", "BMW", "Honda"];
//push method is use to push the data in the end of the array
carCompany.push("Audi");
carCompany.push("BYD");
carCompany.push("Jeep");
console.log(carCompany);

//pop method is use to pop the last element of the array
carCompany.pop();
console.log(carCompany)