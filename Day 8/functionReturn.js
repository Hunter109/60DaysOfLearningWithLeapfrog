// we can also return data using function

function sum(a, b) {
    return a + b;
}
function multiple(a, b) {
    return a * b;
}
function divide(a, b) {
    return a / b;
}

let addition = sum(3, 10);
console.log(addition);

let mul = multiple(2, 3);
console.log(mul);

let division = divide(10, 2);
console.log(division);
